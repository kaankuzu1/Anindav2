/**
 * Get the primary language for a country.
 * Accepts ISO 2-letter codes ("US", "DE") or full names ("United States", "Germany").
 * Case-insensitive. Falls back to 'English' for null, undefined, or unknown values.
 */
export function getLanguageFromCountry(country: string | null | undefined): string {
  if (!country) return 'English';
  const key = country.trim().toUpperCase();
  return COUNTRY_LANGUAGE_MAP[key] || 'English';
}
